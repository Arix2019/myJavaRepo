package AppsBasicos;

public class TesteArrayList04 {
    
    private String nome;
    private int idade;
    
    public TesteArrayList04(String nome, int idade){
        this.nome = nome;
        this.idade = idade;
    }
    
    public String getNome(){
        return nome;
    }
    
    public void setNome(String nome){
        this.nome = nome;
    }    
    
    public int getIdade(){
        return idade;
    }
    
    public void setIdade(int idade){
        this.idade = idade;
    }       
    

}
