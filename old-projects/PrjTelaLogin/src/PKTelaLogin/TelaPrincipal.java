    private void jMenRelClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenRelClientesActionPerformed
        // gerando relatorio de clientes:
        int confirma = JOptionPane.showConfirmDialog(null, "Deseja gerar um relatório de "
                + "Clientes?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION) {
            //imprimir o relatorio com o fremework japesReport:
            try {
                // usando a classe jasperPrint p/ preparar a impressao
                JasperPrint jPrint = JasperFillManager.fillReport("C:\\Users\\Arix\\JaspersoftWorkspace\\MyReports\\clientes2.jasper", null, VarConexao);
                // exibe o relatorio atraves da classe jasperViewer
                JasperViewer.viewReport(jPrint, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }
    }//GEN-LAST:event_jMenRelClientesActionPerformed
    
