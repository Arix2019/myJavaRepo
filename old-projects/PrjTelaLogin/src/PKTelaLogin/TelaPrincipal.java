    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // emite as OS que estão relacionadas com nome dos clientes:
        String relPath ="C:\\Users\\Arix\\JaspersoftWorkspace\\MyReports\\EmissaoDeOs.jasper";
        int confirma = JOptionPane.showConfirmDialog(null, "Deseja emitir o Relatório de OS "
                + "Relacionadas?","Atenção", JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION){
            try {
                JasperPrint jPrint = JasperFillManager.fillReport(relPath, null,VarConexao);
                //JasperViewer.viewReport(jPrint,false);
                JasperViewer jv = new JasperViewer(jPrint,false);
                jv.setTitle("Relatório Cliente/OS");
                jv.setVisible(true);
            } catch (JRException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

